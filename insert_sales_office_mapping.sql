UPDATE jst_flat.sales_office_mapping m
INNER JOIN jst.core_dept d
    ON d.dept_id = LPAD(m.dept_id, 8, '0')
SET m.dept_id   = d.dept_id,
    m.dept_name = d.dept_name;

